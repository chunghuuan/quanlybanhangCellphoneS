    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        if ((tblmodel.getRowCount()) < 1) {
            JOptionPane.showMessageDialog(this, "Bạn chưa tạo sản phẩm, vui lòng tạo ít nhất 1 sản phẩm !");
            return;
        }
        if (tblHoaDon.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(null, "Vui lòng click chọn 1 dòng trong table !");
            return;
        } else {
            String SLthaydoi = JOptionPane.showInputDialog(this, "Nhập số lượng cần thay đổi", "Thông báo", JOptionPane.QUESTION_MESSAGE);

            while (SLthaydoi.equals("")) {
                JOptionPane.showMessageDialog(this, "Không được để trống !");
                SLthaydoi = JOptionPane.showInputDialog(this, "Nhập số lượng cần thay đổi", "Thông báo", JOptionPane.QUESTION_MESSAGE);
            }

            if (Integer.valueOf(SLthaydoi) <= 0) {
                JOptionPane.showMessageDialog(this, "Số lượng nhập vào phải lớn hơn 0 !");
                return;
            }
            tblmodel.setValueAt(SLthaydoi, tblHoaDon.getSelectedRow(), 2);

            int giaBan = Integer.valueOf(xldh.xoaPhay(tblmodel.getValueAt(tblHoaDon.getSelectedRow(), 4).toString()).trim());
            String ThTmoi = xldh.themPhay(String.valueOf((Integer.parseInt(SLthaydoi)) * giaBan));

            tblmodel.setValueAt(ThTmoi, tblHoaDon.getSelectedRow(), 5);
        }
    }//GEN-LAST:event_btnSuaActionPerformed

