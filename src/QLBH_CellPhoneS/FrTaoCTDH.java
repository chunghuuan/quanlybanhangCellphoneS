    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed

        int ques = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn tạo đơn hàng?", "Thông báo", JOptionPane.YES_NO_OPTION);
        if (ques == 0) {
            for (int i = 0; i < tblmodel.getRowCount(); i++) {
                String MaDH = lbMaDH.getText();
                String MaSP = tblmodel.getValueAt(i, 0).toString().trim();
                String SLMua = tblmodel.getValueAt(i, 2).toString().trim();
                String maHT = xldh.lay1DL1BangcoDK("MaBH", "ChinhSachBaoHanh", "TenCSBH", cbxHTBH1.getSelectedItem().toString().trim());
                xldh.them1SP(MaDH, MaSP, SLMua, maHT);
            }
            JOptionPane.showMessageDialog(this, "Thêm sản phẩm thành công, nhấn OK để xem chi tiết Đơn Hàng !");

            if (pq.getKey().equals("2")) {
                pq.setXacnhan("1");
                new FrXemCTDH(lbMaDH.getText(), pq, "2").setVisible(true);
            } else if (pq.getKey().equals("1")) {
                pq.setXacnhan(null);
                new FrXemCTDH(lbMaDH.getText(), pq, "1").setVisible(true);
            }
            this.setVisible(false);

        }
    }//GEN-LAST:event_btnLuuActionPerformed

